func artefactsCmd() *cobra.Command {
	cmd := &cobra.Command{
		Use:     "artefacts",
		Aliases: []string{"art"},
		Short:   "Manage agent artefacts",
		Long: `Manage files generated by AI Agent iterations.

ARTEFACT CONSTRAINTS:
  - Artefacts securely embed massive context responses, markdown scripts, or JSON configs into the Agent brain without polluting the chat logs.
  - LLM Genies MUST bundle any text output over ~40 lines into an Artefact instead of direct chat messages to keep the session context token window efficient.

SUBCOMMANDS:
  list <agent-id-or-name>          List artefacts
  get <agent-id-or-name> <id-or-name> Show artefact details
  create <agent-id-or-name>        Upload an artefact
  update <id-or-name>              Update artefact metadata or player-owned body
  download <agent-id-or-name> <id-or-name> Download artefact file content`,
	}
	cmd.AddCommand(artListCmd(), artGetCmd(), artCreateCmd(), artUpdateCmd(), artDownloadCmd())
	return cmd
}

func artListCmd() *cobra.Command {
	var query, name, agentIDFlag, playgroundID, contentType, createdAfter, createdBefore, sort string
	cmd := &cobra.Command{
		Use: "list [agent-id-or-name]", Short: "List artefacts", Args: cobra.MaximumNArgs(1),
		Long: `List artefacts. When agent-id-or-name is provided, lists artefacts for that agent.
When omitted, lists all artefacts accessible by the current player.

FILTERS:
  -q, --query           Search across name, description (substring match)
  --name                Filter by name (substring match)
  --agent-id            Filter by agent ID or name (when listing all)
  --playground-id       Filter by playground ID or name
  --content-type        Filter by content type

DATE RANGE:
  --created-after       Show items created on or after this date (ISO 8601)
  --created-before      Show items created on or before this date (ISO 8601)

SORTING:
  --sort                Sort results. Format: {column}_{direction}
                        Columns: created_at, name
                        Direction: asc, desc
                        Default: created_at_desc

EXAMPLES:
  fibe artefacts list
  fibe artefacts list my-agent
  fibe artefacts list -q "report" --sort name_asc
  fibe artefacts list --agent-id builder --content-type application/pdf -o json`,
		RunE: func(cmd *cobra.Command, args []string) error {
			c := newClient()
			params := &fibe.ArtefactListParams{}
			if query != "" {
				params.Query = query
			}
			if name != "" {
				params.Name = name
			}
			if playgroundID != "" {
				params.PlaygroundID = playgroundID
			}
			if contentType != "" {
				params.ContentType = contentType
			}
			if createdAfter != "" {
				params.CreatedAfter = createdAfter
			}
			if createdBefore != "" {
				params.CreatedBefore = createdBefore
			}
			if sort != "" {
				params.Sort = sort
			}
			if flagPage > 0 {
				params.Page = flagPage
			}
			if flagPerPage > 0 {
				params.PerPage = flagPerPage
			}

			if len(args) > 0 {
				arts, err := c.Artefacts.ListByAgentIdentifier(ctx(), args[0], params)
				if err != nil {
					return err
				}
				outputJSON(arts)
			} else {
				if agentIDFlag != "" {
					params.AgentID = agentIDFlag
				}
				arts, err := c.Artefacts.ListAll(ctx(), params)
				if err != nil {
					return err
				}
				outputJSON(arts)
			}
			return nil
		},
	}
	cmd.Flags().StringVarP(&query, "query", "q", "", "Search across name, description")
	cmd.Flags().StringVar(&name, "name", "", "Filter by name (substring)")
	cmd.Flags().StringVar(&agentIDFlag, "agent-id", "", "Filter by agent ID or name (when listing all)")
	cmd.Flags().StringVar(&playgroundID, "playground-id", "", "Filter by playground ID or name")
	cmd.Flags().StringVar(&contentType, "content-type", "", "Filter by content type")
	cmd.Flags().StringVar(&createdAfter, "created-after", "", "Filter: created after date (ISO 8601)")
	cmd.Flags().StringVar(&createdBefore, "created-before", "", "Filter: created before date (ISO 8601)")
	cmd.Flags().StringVar(&sort, "sort", "", "Sort order (e.g. created_at_desc)")
	return cmd
}

