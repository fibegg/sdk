func artDownloadCmd() *cobra.Command {
	var to string
	cmd := &cobra.Command{
		Use:   "download <agent-id-or-name> <id-or-name>",
		Short: "Download an artefact's file content",
		Long: `Download the binary content of an artefact.

REQUIRED FLAGS:
  --to    Output file path (use - for stdout)

EXAMPLES:
  fibe artefacts download my-agent report --to ./report.pdf
  fibe artefacts download my-agent report --to - > report.pdf`,
		Args: cobra.ExactArgs(2),
		RunE: func(cmd *cobra.Command, args []string) error {
			c := newClient()
			if to == "" {
				return fmt.Errorf("required field 'to' not set")
			}
			body, filename, contentType, err := c.Artefacts.DownloadByAgentAndArtefactIdentifier(ctx(), args[0], args[1])
			if err != nil {
				return err
			}
			defer body.Close()

			var dst io.Writer
			if to == "-" {
				dst = os.Stdout
			} else {
				f, err := os.Create(to)
				if err != nil {
					return fmt.Errorf("create output file: %w", err)
				}
				defer f.Close()
				dst = f
			}

			n, err := io.Copy(dst, body)
			if err != nil {
				return fmt.Errorf("write content: %w", err)
			}
			if to != "-" {
				if filename == "" {
					filename = "(unknown)"
				}
				fmt.Fprintf(os.Stderr, "Wrote %d bytes (filename: %s, content-type: %s) to %s\n", n, filename, contentType, to)
			}
			return nil
		},
	}
	cmd.Flags().StringVar(&to, "to", "", "Output file path (required, use - for stdout)")
	return cmd
}

